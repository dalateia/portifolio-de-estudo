// Nesse exemplo é usado uma frase boba para ser conferida

var agora = "poxa";

switch (agora) {
  case "poxa":
    console.log("poxa vida");
    break;
  case "caramba":
    console.log("caramba meu");
    break;
  case "nossa":
    console.log("nossa senhora");
    break;
  case "uhul":
    console.log("uhul mano");
    break;
  default:
    console.log("Que frase é essa?");
    break;
}
